    /**
    * This method is used to add a user to the database with the corresponding structure using the csv
    * @author saimo
    */
    private void Add_user() {
        String archive = Paths.get("src", "DataBase", "usuarios.csv").toString();
        FileWriter fw = null;
        PrintWriter pw = null;
        
        try{ 
            fw = new FileWriter(archive);
            pw = new PrintWriter(fw);
            for(register_users i: main_class.users){
                String line = i.getUsername() + "," + i.getPassword() + "," + i.getRemember() + "," + i.getRecoveryEmail();
                pw.println(line);
            }
            
        }catch (Exception ex) {
            ex.printStackTrace();
        }
        finally{
            try{
                if(fw != null){
                    fw.close();
            }
            }catch(Exception ex){
                    ex.printStackTrace();
            }      
        }
    }
    /**
    * This method is used to create a new user and performs validations so as not to add invalid users to the database
    * @author saimo
    */
    private boolean create_user() {
        
        String create_username;
        String create_password;
        String create_recoveryEmail;
        boolean create_remember;
        Boolean register = false;

        create_username = String.valueOf(UsernameText.getText());
        String username = UsernameText.getText();
        create_recoveryEmail = RecoveryEmail.getText();
        if (username == null || username.length() < 4 || username.length() > 16) {
            JOptionPane.showMessageDialog(this, "Enter a user from 4 characters to 16 characters maximum");
            return false;
        }
        String password = PasswordTextVisible.getText();
        if (password.isEmpty() || password.length() < 8 || password.length() > 32) {
            JOptionPane.showMessageDialog(this, "Enter a valid password of minimum 8 to maximum 32 characters");
            return false;
        }
        if (password.length() < 8) {
            JOptionPane.showMessageDialog(this, "Enter a valid password of minimum 8 to maximum 32 characters");
            return false;
        }
        if (!PasswordTextVisible.getText().equals(String.valueOf(PasswordText.getPassword()))) {
            JOptionPane.showMessageDialog(this,"Password Is Different");
            return false;
        }
        create_password = String.valueOf(PasswordTextVisible.getText());
        create_remember = false;

        for(register_users A: main_class.users){
            if(A.getUsername().equals(create_username)){
                register = true;
                break;}         
            }

            if(register){
                JOptionPane.showMessageDialog(this,"This Username Already Exists ");  
                return false;
            }else{
                register_users user = new register_users();
                user.setUsername(create_username);
                user.setPassword(create_password);
                user.setRemember(create_remember);
                user.setRecoveryEmail(create_recoveryEmail);
                main_class.users.add(user);
                
                Add_user();
                JOptionPane.showMessageDialog(this, "User Added Successfully");

            }
        return true;
    }
